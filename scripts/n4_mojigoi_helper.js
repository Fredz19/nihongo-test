export function getBalancedN4Vocab(vocabList, pkgIdx) {
  // If Package A (index 0), return the official questions from Super Moshi
  if (pkgIdx === 0) {
    return superMoshiN4VocabQuestions.map((q, i) => ({
      ...q,
      id: `mojigoi_n4_a_m${getMondaiForType(q.type)}_q${String(i + 1).padStart(2, '0')}`,
      mondai: getMondaiForType(q.type),
      number: i + 1
    }));
  }

  // Otherwise, compile Package B and C using other questions from the vocabList, avoiding overlap
  const moshiQuestionsSet = new Set(superMoshiN4VocabQuestions.map(q => q.question));
  const filteredVocab = vocabList.filter(q => !moshiQuestionsSet.has(q.question));

  const pools = {
    'kanji-read': filteredVocab.filter(q => q.type === 'kanji-read'),
    'orthography': [...filteredVocab.filter(q => q.type === 'orthography'), ...extraOrthographyN4],
    'context': filteredVocab.filter(q => q.type === 'context'),
    'paraphrase': filteredVocab.filter(q => q.type === 'paraphrase'),
    'usage': [...filteredVocab.filter(q => q.type === 'usage'), ...extraUsageN4]
  };

  const distribution = [
    { type: 'kanji-read', count: 9, mondai: 1 },
    { type: 'orthography', count: 6, mondai: 2 },
    { type: 'context', count: 10, mondai: 3 },
    { type: 'paraphrase', count: 5, mondai: 4 },
    { type: 'usage', count: 5, mondai: 5 }
  ];

  const result = [];
  const letter = pkgIdx === 1 ? 'b' : 'c';
  // Adjust starting offset for Package C (pkgIdx === 2)
  const offsetIdx = pkgIdx - 1; // 0 for B, 1 for C

  let absoluteNumber = 1;
  distribution.forEach(dist => {
    const pool = pools[dist.type];
    const startIdx = offsetIdx * dist.count;
    for (let i = 0; i < dist.count; i++) {
      const itemIdx = (startIdx + i) % pool.length;
      const q = pool[itemIdx];
      result.push({
        ...q,
        id: `mojigoi_n4_${letter}_m${dist.mondai}_q${String(i + 1).padStart(2, '0')}`,
        mondai: dist.mondai,
        number: absoluteNumber++
      });
    }
  });

  return result;
}

function getMondaiForType(type) {
  if (type === 'kanji-read') return 1;
  if (type === 'orthography') return 2;
  if (type === 'context') return 3;
  if (type === 'paraphrase') return 4;
  if (type === 'usage') return 5;
  return 1;
}
